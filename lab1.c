#include <stdio.h>

/*
 * Функция max:
 * принимает два числа типа long long
 * возвращает большее из них.
 */
long long max(long long a, long long b) { 
    return a > b ? a : b; 
}

/*
 * Функция next_prime_factor:
 *  - Аргумент: указатель на число *x.
 *  - Внутри есть статическая переменная p — текущий простой делитель (начинаем с 2).
 *
 * Смысл:
 *  1) Ищет следующий делитель p числа *x (p растёт: 2,3,4,5,...).
 *  2) Как только находит p, который делит *x, делит *x на p до конца (убирает p^k).
 *  3) Возвращает найденный простой делитель p.
 *
 * Если при этом p^2 > *x, значит:
 *   *x либо 1, либо само по себе простое число.
 * Тогда функция возвращает это остаточное простое число (*x),
 * и устанавливает *x = 1, чтобы показать, что разложение закончено.
 *
 * В итоге:
 *   - Каждый вызов возвращает ОДИН простой множитель числа.
 *   - В *x остаётся число без всех найденных степеней этого множителя.
 */
long long next_prime_factor(long long *x) {
    static long long p = 2;        // текущий возможный делитель (сохраняется между вызовами)

    // Ищем p, который делит *x, пока p^2 <= *x
    while (p * p <= *x && *x % p != 0) 
        p++;                       // увеличиваем p, пока не найдём делитель

    // Если p^2 > *x — больше делителей искать не нужно
    if (p * p > *x) {              // *x либо 1, либо простое
        long long last = *x;       // запоминаем остаток
        *x = 1;                    // помечаем, что число полностью разложено
        return last;               // возвращаем оставшийся множитель (может быть 1)
    }

    // Здесь p делит *x → удаляем все p из *x (делим до конца)
    while (*x % p == 0) 
        *x /= p;

    return p;                      // возвращаем найденный простой делитель p
}

int main() {
    long long A, B;    // A и B — заданные числа из условия
    printf("Соколов Кирилл 090304-РПИа025\n");
    if (scanf("%lld %lld", &A, &B) != 2) 
        return 0;      // если не удалось прочитать два числа — выходим
    
    if (!(2 <= A && A <= 2000000000LL &&
          2 <= B && B <= 2000000000LL)) {
        printf("-1\n");   // тут было "\\n"
        return 0;
    }
    
    long long a = A;   // копия числа A, которую будем разлагать на простые множители
    long long ans = 1; // здесь будем хранить искомое минимальное n

    // Разлагаем A на простые множители, последовательно беря каждый простой делитель p
    while (a > 1) {
        long long p = next_prime_factor(&a);  // берём следующий простой делитель A
        if (p == 1) break;                    // если вернули 1 — разложение закончено

        // Считаем степень alpha простого числа p в разложении A
        long long alpha = 0, tmp = A;
        while (tmp % p == 0) { 
            tmp /= p; 
            alpha++; 
        }

        // Считаем степень beta того же p в разложении B
        long long beta = 0; 
        tmp = B;
        while (tmp % p == 0) { 
            tmp /= p; 
            beta++; 
        }

        // Если p входит в A, но вообще не входит в B (beta == 0),
        // то B^n не сможет "накрыть" p^alpha, делимости не будет никогда.
        if (beta == 0) {      
            printf("-1\n");   // тут было "\\n"
            return 0;
        }

        // Нужно beta * n >= alpha → n >= alpha / beta (с округлением вверх).
        long long need = (alpha + beta - 1) / beta;

        // Обновляем ответ: он должен быть максимальным из всех need по разным p.
        ans = max(ans, need);
    }

    // После учёта всех простых делителей A в ans лежит минимальное n, при котором B^n делится на A.
    printf("%lld\n", ans);   // тут было "\\n"
    return 0;
}

